package com.company;

public class Tipos {

    public static void main(String[] args) {

        //Enteros
        int numeroEntero = 5;
        long numeroLong = 5000000000000000l;


        //Decimales
        float decimalFloat =  7.8f;
        double decimalDouble = 5.5d;

        //booleanos
        boolean verdadero = true;
        boolean falso = false;


        //caracteres y cadenas de texto
        char caracter = 'r';
        String texto = "Soy una casdena de texto";

        System.out.println("Número int: " + numeroEntero);
        System.out.println("Número long: " + numeroLong);
        System.out.println("Número decimal float: " + decimalFloat);
        System.out.println("Número decimal double: " + decimalDouble);
        System.out.println("booleano verdadero: " + verdadero);
        System.out.println("booleano falso: " + falso);
        System.out.println("caracter: " + caracter);
        System.out.println("cadena de texto: " + texto);
    }

}
